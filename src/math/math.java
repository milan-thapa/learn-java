package math;



public class math {
    public static void  main(String[] args){


        System.out.println("Maximum Value between two numbers: " + Math.max(5,10));
        System.out.println("Square of given number : " +  Math.sqrt(25));
        System.out.println("Absulote value :" +   Math.abs(-2.7));
        System.out.println(" Power of a given number : " +  Math.pow(2,8) );
        System.out.println("Round Value : " +   Math.round(4.6));
        System.out.println("Ceil Value : " +  Math.ceil(4.1));
        System.out.println("Floor Value :" +  Math.floor(4.9));
        System.out.println("Random Value : " +  Math.random());

    }
}
