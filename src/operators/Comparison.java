package operators;

public class Comparison {
    public static void main(String[] args) {

        int x = 100;
        int y = 50;

        // Comparison operators
        System.out.println("x > y: " + (x > y)); // true
        System.out.println("x < y: " + (x < y)); // false
        System.out.println("x == y: " + (x == y)); // false
        System.out.println("x != y: " + (x != y)); // true
        System.out.println("x >= y: " + (x >= y)); // true
        System.out.println("x <= y: " + (x <= y)); // false
    }
}